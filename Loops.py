numbers = [10, 20, 30, 40, 50]

# for each
for number in numbers:
    print(number)

print()
# range(n), from 0 to n-1
for i in range(5):
    print(numbers[i])
print()
# range(m, n), from m to n-1
for i in range(1, 3):
    print(numbers[i])
print()
# range(i, j, k), from i to j-1, increment by k
for i in range(1, 10, 2):
    print(i)



